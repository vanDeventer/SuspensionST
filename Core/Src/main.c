/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	uint8_t buf[40];
	int16_t val;
	float temp_c;

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_USART2_UART_Init();
  MX_I2C1_Init();
  MX_TIM3_Init();
  /* USER CODE BEGIN 2 */
	HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_3);
	HAL_ADC_Start_IT(&hadc1);
	strcpy((char*)buf, "\n Automotive Systems 2: Suspension demo\r\n");
	HAL_UART_Transmit(&huart2, buf, strlen((char*)buf), HAL_MAX_DELAY);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
		HAL_GPIO_TogglePin (GPIOA, GPIO_PIN_5);  /* To show that the system is alive */
		switch (Suspension){
			case LOWERING:
				HAL_GPIO_TogglePin(LoweredLed_GPIO_Port, LoweredLed_Pin);
				break;
			case RAISING:
				HAL_GPIO_TogglePin(LoweredLed_GPIO_Port, LoweredLed_Pin);
				break;
			case NORMAL:
				HAL_GPIO_WritePin(LoweredLed_GPIO_Port, LoweredLed_Pin, GPIO_PIN_RESET);
				break;
			case LOW:
				HAL_GPIO_WritePin(LoweredLed_GPIO_Port, LoweredLed_Pin, GPIO_PIN_SET);
				break;
			default:
				break;
		}
		if (Malfunction) {
			HAL_GPIO_TogglePin(MalfunctionLed_GPIO_Port, MalfunctionLed_Pin);
		}
		HAL_Delay (250); /* Insert delay 250 ms */
		
		// Tell TMP_102 that we wat to read from the temperature register
		buf[0] = REG_TEMP;
		if (HAL_I2C_Master_Transmit(&hi2c1, TMP_102_ADDR, buf, 2 , HAL_MAX_DELAY) != HAL_OK) {
			strcpy((char*)buf, "Error in I2C Tx\r\n");
		} else {
			if (HAL_I2C_Master_Receive(&hi2c1, TMP_102_ADDR, buf, 2, HAL_MAX_DELAY) != HAL_OK) {
				strcpy((char*)buf, "Error in I2C Rx\r\n");
			} else {
				val = ((int16_t)buf[0] << 4) | (buf[1] >> 4);
				if (val > 0x7FF) {
					val |=0xF000;
				}
				temp_c = val * 0.0625;
				temp_c *= 100;
				sprintf((char*)buf, "%u.%u C\r\n", ((unsigned int)temp_c/100), ((unsigned int)temp_c%100));
			}
		}
		HAL_UART_Transmit(&huart2, buf, strlen((char*)buf), HAL_MAX_DELAY);
		sprintf((char*)buf, "%u Hz\r\n", uwFrequency);
		HAL_UART_Transmit(&huart2, buf, strlen((char*)buf), HAL_MAX_DELAY);
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

